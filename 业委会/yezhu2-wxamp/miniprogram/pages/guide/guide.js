Page({
  data: {
    stages: STAGES,
    currentKey: 'collecting',
    currentNo: 1,
    expanded: -1,
    communityName: ''
  },

  onShow () {
    app.api('getStats').then(s => {
      const key = s.community.currentStage || 'collecting'
      const idx = STAGES.findIndex(x => x.key === key)
      this.setData({
        currentKey: key,
        currentNo: idx >= 0 ? STAGES[idx].no : 1,
        communityName: s.community.communityName,
        expanded: idx >= 0 ? idx : 0
      })
    }).catch(() => {})
  },

  toggle (e) {
    const i = e.currentTarget.dataset.i
    this.setData({ expanded: this.data.expanded === i ? -1 : i })
  },

  go (e) {
    const url = e.currentTarget.dataset.url
    wx.navigateTo({ url })
  }
})
